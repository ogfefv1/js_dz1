let age = parseInt(prompt("Напишите ваш возраст:"));

let category = 
    (age >= 0 && age <= 2) ? "Ребенок" :
    (age > 12 && age <= 18) ? "Подросток" :
    (age > 18 && age <= 60) ? "Взрослый" :
    (age > 60) ? "Пенсионер" :
    "Ошибка: некорректный возраст";

alert(`Вы относитесь к категории: ${category}`);